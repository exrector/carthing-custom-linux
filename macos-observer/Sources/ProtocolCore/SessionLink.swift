import Foundation

/// Транспорт-агностичный байтовый канал.
///
/// CTSP не должен знать, что под ним — L2CAP CoC, TCP-loopback или USB. Это
/// позволяет гонять одну и ту же логику протокола по разным транспортам
/// («тот же логический протокол по другим транспортам» из архитектуры).
public protocol SessionLink: AnyObject {
    /// Пришли сырые байты из канала.
    var onBytes: ((Data) -> Void)? { get set }
    /// Канал поднялся (true) / упал (false).
    var onStateChange: ((Bool) -> Void)? { get set }
    /// Сообщение об ошибке транспорта.
    var onError: ((String) -> Void)? { get set }

    func start()
    func send(_ data: Data)
    func stop()
}
